static int32_t s5k8aayx_sensor_setting(struct msm_sensor_ctrl_t *s_ctrl,
			int update_type, int res)
{
	int32_t rc = 0;

	s_ctrl->func_tbl->sensor_stop_stream(s_ctrl);
	msleep(30);
	if (update_type == MSM_SENSOR_REG_INIT) {
		s_ctrl->curr_csi_params = NULL;
		msm_sensor_enable_debugfs(s_ctrl);
		CDBG("%s msm_sensor_write_init_settings start\n", __func__);
		msm_sensor_write_init_settings(s_ctrl);
		CDBG("%s msm_sensor_write_init_settings end\n", __func__);
		CDBG("%s s5k8aayx_sensor_write_init_settings start\n", __func__);
		s5k8aayx_sensor_write_init_settings(s_ctrl);
		CDBG("%s s5k8aayx_sensor_write_init_settings end\n", __func__);
	} else if (update_type == MSM_SENSOR_UPDATE_PERIODIC) {
		CDBG("%s s5k8aayx_sensor_write_prev_settings start\n", __func__);
		s5k8aayx_sensor_write_prev_settings(s_ctrl, res);
		CDBG("%s s5k8aayx_sensor_write_prev_settings end\n", __func__);
		if (s_ctrl->curr_csi_params != s_ctrl->csi_params[res]) {
			s_ctrl->curr_csi_params = s_ctrl->csi_params[res];
			s_ctrl->curr_csi_params->csid_params.lane_assign =
				s_ctrl->sensordata->sensor_platform_info->
				csi_lane_params->csi_lane_assign;
			s_ctrl->curr_csi_params->csiphy_params.lane_mask =
				s_ctrl->sensordata->sensor_platform_info->
				csi_lane_params->csi_lane_mask;
			v4l2_subdev_notify(&s_ctrl->sensor_v4l2_subdev,
				NOTIFY_CSID_CFG,
				&s_ctrl->curr_csi_params->csid_params);
			mb();
			v4l2_subdev_notify(&s_ctrl->sensor_v4l2_subdev,
				NOTIFY_CSIPHY_CFG,
				&s_ctrl->curr_csi_params->csiphy_params);
			mb();
			msleep(20);
		}

		v4l2_subdev_notify(&s_ctrl->sensor_v4l2_subdev,
			NOTIFY_PCLK_CHANGE, &s_ctrl->msm_sensor_reg->
			output_settings[res].op_pixel_clk);
		s_ctrl->func_tbl->sensor_start_stream(s_ctrl);
		msleep(30);
	}
	return rc;
}

